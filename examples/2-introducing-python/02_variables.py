# Defining variables in Python means that the data referred to by the variable
# has a type, but the variable itself doesn't have a type -- if you reassign
# that variable to a different type of data, it's fine.

# An integer is any whole number
my_int = 1
type(my_int)

# A float is any number that needs a decimal point
my_float = 1.5
type(my_float)

# A string is a series of zero or more characters
my_string = 'hello!'
type(my_string)

my_string = ''
type(my_string)

my_string = 'a'
type(my_string)
