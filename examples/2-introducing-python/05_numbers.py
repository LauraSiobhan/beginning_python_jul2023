# Math works much like you'd expect
first_number = 12
second_number = 8

# Addition
print(first_number + second_number)

# Subtraction
print(first_number - second_number)

# Multiplication
print(first_number * second_number)

# Division
print(first_number / second_number)


# Putting strings and numbers together can be problematic
print('My first number is: ' + first_number)
print('My second number is: ' + second_number)

# Numbers can be converted to strings, though:
print('My first number is: ' + str(first_number))
print('My second number is: ' + str(second_number))

